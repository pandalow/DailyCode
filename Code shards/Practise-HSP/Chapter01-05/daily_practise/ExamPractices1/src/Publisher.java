
public class Publisher {

	private String name;
	private String address;
	
	public Publisher() {}
	
	public Publisher(String name, String address) {
		this.name = name;
		this.address = address;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAddress() {
		return address;
	}
	
	public String toString() {
		return "1";
	}
}
